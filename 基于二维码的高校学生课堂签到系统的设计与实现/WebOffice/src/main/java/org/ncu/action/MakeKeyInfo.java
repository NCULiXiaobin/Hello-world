package org.ncu.action;

public class MakeKeyInfo {
    private String makeUseAccount;
    private String makeUseMajor;
    private String selectClass;
    private String makeUseCurse;
    private String week;
    private int indexCurse;

    public String getMakeUseAccount() {
        return makeUseAccount;
    }

    public void setMakeUseAccount(String makeUseAccount) {
        this.makeUseAccount = makeUseAccount;
    }

    public String getMakeUseMajor() {
        return makeUseMajor;
    }

    public void setMakeUseMajor(String makeUseMajor) {
        this.makeUseMajor = makeUseMajor;
    }

    public String getSelectClass() {
        return selectClass;
    }

    public void setSelectClass(String selectClass) {
        this.selectClass = selectClass;
    }

    public String getMakeUseCurse() {
        return makeUseCurse;
    }

    public void setMakeUseCurse(String makeUseCurse) {
        this.makeUseCurse = makeUseCurse;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public int getIndexCurse() {
        return indexCurse;
    }

    public void setIndexCurse(int indexCurse) {
        this.indexCurse = indexCurse;
    }
}
