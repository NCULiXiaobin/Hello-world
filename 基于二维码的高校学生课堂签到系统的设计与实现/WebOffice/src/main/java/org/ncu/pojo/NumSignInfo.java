package org.ncu.pojo;

public class NumSignInfo {
    private String numSignMajor;
    private String numSignClassName;

    public String getNumSignMajor() {
        return numSignMajor;
    }

    public void setNumSignMajor(String numSignMajor) {
        this.numSignMajor = numSignMajor;
    }

    public String getNumSignClassName() {
        return numSignClassName;
    }

    public void setNumSignClassName(String numSignClassName) {
        this.numSignClassName = numSignClassName;
    }
}
